function DoiTien(price) {
	var result=[];
	var arrT=price.toString().split('');
	arrT.reverse();
	var t=0;
	for (var j=0;j<=Math.floor(arrT.length/3);j++)
	{
		if (t+3>arrT.length-1)
			result.push(arrT.slice(t).reverse().join(''));
		else 
		{
			result.push(arrT.slice(t,t+3).reverse().join(''),'.');
		}
		t+=3;
	}
	result.reverse();
	return result;
}

